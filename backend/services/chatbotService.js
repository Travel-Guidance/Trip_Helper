let genAI = null;

function getGenAI() {
  if (!genAI) {
    const key = process.env.GEMINI_CHATBOT_KEY;
    if (!key || key === '여기에_챗봇용_API_키_입력') {
      throw new Error('GEMINI_CHATBOT_KEY가 설정되지 않았습니다.');
    }
    genAI = new GoogleGenerativeAI(key);
  }
  return genAI;
}

async function chat(message, history = []) {
  const model = getGenAI().getGenerativeModel({
    model: process.env.CHAT_MODEL || 'gemini-2.5-flash-lite',
    systemInstruction: SYSTEM_PROMPT,
  });

  // Gemini requires history to start with 'user' role
  const firstUserIdx = history.findIndex((h) => h.role === 'user')
  const trimmed = firstUserIdx >= 0 ? history.slice(firstUserIdx) : []

  const geminiHistory = trimmed.map((h) => ({
    role: h.role,
    parts: [{ text: h.text }],
  }));

  const chatSession = model.startChat({ history: geminiHistory });
  const result = await chatSession.sendMessage(message);
  return result.response.text();
}

module.exports = { chat };
