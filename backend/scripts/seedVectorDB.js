/* ─── 메인 실행 ────────────────────────────────────────── */
async function seed() {
  console.log('=== Qdrant Vector DB 시드 시작 (호주 집중) ===\n');

  await recreateCollection();

  const total = KNOWLEDGE.length;
  console.log(`\n총 ${total}개 항목 임베딩 시작...\n`);

  for (let i = 0; i < KNOWLEDGE.length; i++) {
    const item = KNOWLEDGE[i];
    try {
      const vector = await getEmbedding(item.text);

      await qdrant(`/collections/${COLLECTION}/points?wait=true`, 'PUT', {
        points: [{
          id: i + 1,
          vector,
          payload: {
            destination:  item.destination,
            city:         item.city,
            category:     item.category,
            text:         item.text,
            lat:          item.lat          ?? null,
            lng:          item.lng          ?? null,
            price_range:  item.price_range  ?? null,
            tags:         item.tags         ?? [],
            hours:        item.hours        ?? null,
          },
        }],
      });

      console.log(`[${i + 1}/${total}] ✓ ${item.city} — ${item.category}`);

      if (i < KNOWLEDGE.length - 1) await sleep(300);
    } catch (err) {
      console.error(`[${i + 1}/${total}] ✗ ${item.city} — ${item.category}: ${err.message}`);
    }
  }

  const info = await qdrant(`/collections/${COLLECTION}`);
  console.log(`\n=== 완료 ===`);
  console.log(`저장된 벡터 수: ${info.result.points_count}`);
}

seed().catch(console.error);
