/* ─── Qdrant 헬퍼 ──────────────────────────────────────── */
async function qdrant(path, method = 'GET', body = null) {
  const res = await fetch(`${QDRANT_URL}${path}`, {
    method,
    headers: { 'Content-Type': 'application/json' },
    body: body ? JSON.stringify(body) : null,
  });
  if (!res.ok) throw new Error(`Qdrant ${method} ${path} → ${res.status}: ${await res.text()}`);
  return res.json();
}

async function recreateCollection() {
  try {
    await qdrant(`/collections/${COLLECTION}`, 'DELETE');
    console.log('✓ 기존 컬렉션 삭제');
  } catch { /* 없으면 그냥 진행 */ }

  await qdrant(`/collections/${COLLECTION}`, 'PUT', {
    vectors: { size: VECTOR_SIZE, distance: 'Cosine' },
  });
  console.log(`✓ 컬렉션 "${COLLECTION}" 생성 완료`);
}

async function getEmbedding(text) {
  const result = await embeddingModel.embedContent(text);
  return result.embedding.values;
}

